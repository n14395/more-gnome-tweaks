CATEGORY_INDEX = {category.id: index for index, category in enumerate(CATEGORIES)}
CHILD_CATEGORIES: dict[str, set[str]] = {}
for _cat in CATEGORIES:
    if _cat.parent is not None:
        CHILD_CATEGORIES.setdefault(_cat.parent, set()).add(_cat.id)
TWEAK_INDEX = {tweak.id: index for index, tweak in enumerate(TWEAKS)}
TWEAK_ORDER_INDEX = {
    category_id: {tweak_id: index for index, tweak_id in enumerate(tweak_ids)}
    for category_id, tweak_ids in CATEGORY_TWEAK_ORDER.items()
}

def filter_tweaks(query: str, category_id: str | None = None) -> list[Tweak]:
    normalized = query.strip().lower()
    results: list[Tweak] = []

    if category_id:
        match_ids = {category_id} | CHILD_CATEGORIES.get(category_id, set())
    else:
        match_ids = None

    for tweak in TWEAKS:
        if match_ids and tweak.category not in match_ids:
            continue

        haystack = " ".join(
            (
                tweak.name,
                tweak.summary,
                tweak.description,
                tweak.schema,
                tweak.key,
                " ".join(tweak.tags),
            )
        ).lower()

        if normalized and normalized not in haystack:
            continue

        results.append(tweak)

    def sort_key(tweak: Tweak) -> tuple[int, int, int, str]:
        category_rank = CATEGORY_INDEX.get(tweak.category, len(CATEGORY_INDEX))
        preferred_rank = TWEAK_ORDER_INDEX.get(tweak.category, {}).get(tweak.id, 10_000)
        fallback_rank = TWEAK_INDEX.get(tweak.id, 10_000)
        return (category_rank, preferred_rank, fallback_rank, tweak.name.lower())

    results.sort(key=sort_key)
    return results
